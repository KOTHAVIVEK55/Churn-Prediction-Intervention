const twilio = require('twilio');
const PDFDocument = require('pdfkit');
const fs = require('fs');
const path = require('path');
const axios = require('axios');
const FormData = require('form-data');

let client;
try {
  if (process.env.TWILIO_ACCOUNT_SID && process.env.TWILIO_AUTH_TOKEN) {
    client = twilio(process.env.TWILIO_ACCOUNT_SID, process.env.TWILIO_AUTH_TOKEN);
  }
} catch (error) {
  console.warn('Twilio client initialization failed. Please check your credentials in .env');
}

exports.sendChurnAlert = async (req, res) => {
  try {
    const { studentPhone, riskCategory, riskPercentage, insights } = req.body;

    if (!studentPhone) {
      return res.status(400).json({ message: 'Student phone number is required' });
    }

    if (!client) {
      return res.status(500).json({ 
        message: 'Twilio is not configured. Please add TWILIO_ACCOUNT_SID and TWILIO_AUTH_TOKEN to .env' 
      });
    }

    const fromNumber = process.env.TWILIO_PHONE_NUMBER || 'whatsapp:+14155238886';
    const toNumber = studentPhone.startsWith('whatsapp:') ? studentPhone : `whatsapp:${studentPhone}`;

    console.log(`[DEBUG] Generating PDF for ${toNumber}...`);

    // 1. Generate PDF
    // Ensure pdfs directory exists
    const pdfDir = path.join(__dirname, '..', 'public', 'pdfs');
    if (!fs.existsSync(pdfDir)) {
      fs.mkdirSync(pdfDir, { recursive: true });
    }
    
    const pdfPath = path.join(pdfDir, `report_${Date.now()}.pdf`);
    const doc = new PDFDocument({ margin: 50 });
    const writeStream = fs.createWriteStream(pdfPath);
    doc.pipe(writeStream);

    // Header Polygon Design (Cream / Green / Orange)
    doc.rect(0, 0, 612, 120).fill('#FEF8E7'); // Cream background
    doc.polygon([612, 0], [450, 120], [612, 120]);
    doc.fill('#1B5E20'); // Green triangle
    doc.polygon([612, 0], [550, 0], [450, 120], [500, 120]);
    doc.fill('#E65100'); // Orange stripe

    // Logo
    const logoPath = path.join(__dirname, '..', 'public', 'lghs_logo.png');
    if (fs.existsSync(logoPath)) {
        // Position it nicely in the header
        doc.image(logoPath, 40, 25, { width: 70 });
    }

    // Title text inside header
    doc.fillColor('#1B5E20') // Dark green text since background is cream
       .fontSize(24)
       .text('LGHS Student', 130, 40)
       .text('Analytics Report', 130, 65);

    doc.fillColor('black');
    doc.moveDown(4);

    const currentDate = new Date().toLocaleDateString('en-US', { year: 'numeric', month: 'long', day: 'numeric' });
    doc.fontSize(12).text(`Date: ${currentDate}`, { align: 'left' });
    doc.moveDown(2);

    doc.fontSize(14).font('Helvetica-Bold').text('Dear Parent / Guardian,', { align: 'left' });
    doc.moveDown(1);

    doc.fontSize(12).font('Helvetica')
       .text(`We are writing this letter on behalf of Little Genius High School Management. The purpose of this notice is to inform you about your child's recent academic and behavioral performance evaluation, generated by our AI Analytics System.`, { align: 'justify', lineGap: 5 });
    
    doc.moveDown(2);

    // Risk Box
    const currentY = doc.y;
    doc.rect(50, currentY, 512, 80).fillAndStroke('#F5F5F5', '#DDDDDD');
    doc.fillColor('black').font('Helvetica-Bold').text(`Risk Category: ${riskCategory}`, 70, currentY + 20);
    doc.text(`Risk Percentage: ${riskPercentage}%`, 70, currentY + 45);
    doc.moveDown(4);

    doc.font('Helvetica-Bold').text(`Areas for Improvement:`, 50, doc.y);
    doc.moveDown(0.5);
    doc.font('Helvetica-Oblique').text(`${insights}`, { align: 'justify', lineGap: 5 });

    doc.moveDown(2);
    doc.font('Helvetica').text(`We appreciate your understanding and support in this matter. Please feel free to contact the school administration for further discussion regarding this report.`, { align: 'justify', lineGap: 5 });

    doc.moveDown(2);
    doc.text('Thank you for your kind attention to this matter.');
    doc.moveDown(2);
    doc.text('Sincerely,');
    doc.moveDown(1);
    doc.font('Helvetica-Bold').text('LGHS Management');
    doc.font('Helvetica').text('Little Genius High School');
    doc.text('Korutla, Dist: Jagityal');

    // Add string border around the entire page
    doc.rect(20, 20, doc.page.width - 40, doc.page.height - 40).strokeColor('#1B5E20').lineWidth(2).stroke();
    doc.rect(25, 25, doc.page.width - 50, doc.page.height - 50).strokeColor('#E65100').lineWidth(1).stroke();

    doc.end();

    await new Promise((resolve, reject) => {
      writeStream.on('finish', resolve);
      writeStream.on('error', reject);
    });

    console.log(`[DEBUG] PDF generated. Uploading to public URL...`);

    // 2. Upload to tmpfiles.org for public Twilio access
    const form = new FormData();
    form.append('file', fs.createReadStream(pdfPath));
    const uploadRes = await axios.post('https://tmpfiles.org/api/v1/upload', form, {
      headers: form.getHeaders()
    });
    
    const rawUrl = uploadRes.data.data.url;
    // For direct download via tmpfiles.org, insert '/dl' in the URL path. 
    const mediaUrl = rawUrl.replace('tmpfiles.org/', 'tmpfiles.org/dl/');

    console.log(`[DEBUG] Media URL generated: ${mediaUrl}`);

    // 3. Send WhatsApp message
    const hour = new Date().getHours();
    const greeting = hour < 12 ? 'Good Morning' : (hour < 18 ? 'Good Afternoon' : 'Good Evening');

    const messageBody = `*Little Genius High School, Korutla* 🏫\n\n` +
                        `${greeting} Parent,\n\n` +
                        `Above is your child's detailed AI Analytics Report in PDF format. ` +
                        `Please review the attached document carefully.\n\n` +
                        `For better guidance and to support your child's progress, please contact LGHS immediately.`;

    const message = await client.messages.create({
      body: messageBody,
      mediaUrl: [mediaUrl],
      from: fromNumber,
      to: toNumber
    });

    console.log(`[DEBUG] Twilio API Response SID: ${message.sid}`);
    
    // Clean up local PDF file
    try {
      fs.unlinkSync(pdfPath);
    } catch (e) {
      console.error('Failed to clean up PDF', e);
    }

    res.status(200).json({ 
      message: 'WhatsApp PDF sent successfully!', 
      messageId: message.sid
    });

  } catch (error) {
    console.error('[DEBUG ERROR] Twilio PDF failed:', error);
    res.status(500).json({ message: 'Failed to send WhatsApp message', error: error.message });
  }
};
